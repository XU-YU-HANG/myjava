package xuyuhang;

public class FangJian {
    private int FangHao;
    private String LeiXing;
    private boolean KongXian;

    public FangJian() {
    }
    public FangJian(int fangHao,String LeiXing,boolean KongXian) {
    }

    public int getFangHao() {
        return FangHao;
    }

    public void setFangHao(int fangHao) {
        FangHao = fangHao;
    }

    public String getLeiXing() {
        return LeiXing;
    }

    public void setLeiXing(String leiXing) {
        LeiXing = leiXing;
    }

    public boolean isKongXian() {
        return KongXian;
    }

    public void setKongXian(boolean kongXian) {
        KongXian = kongXian;
    }
}

